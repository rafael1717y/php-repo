<?php
// number in pounds we want to convert do kilograms
$pounds = 140;

// float point value for the pound to kilogram conversion
$lb_to_kg = 0.45392;

// use the variables above to calculate pounds multiplied by the kilogram conversion
$kilograms = $pounds * $lb_to_kg;

// display the pounds to kilogram
echo "Weight: ";
echo $pounds;
echo " lb =";
echo $kilograms;
echo " kg";


// number in miles we want to convert do kilometers
$miles = 2.5;

// floating point value for the mile to kilometer conversation
$mile_to_km = 1.60934;

// use the variable above to calculate miles * kilometer conversion
$kilometers = $miles * $mile_to_km;

// display
echo "Distance: ";
echo " miles = ";
echo $kilometers;
echo " km";





?>